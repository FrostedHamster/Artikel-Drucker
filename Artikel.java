
public class Artikel {
	private String bezeichnung;
	private float preis;
	public String getBezeichnung() {
		return bezeichnung;
	}
	public float getPreis() {
		return preis;
	}
	public Artikel(String bezeichnung, float preis) {
		super();
		this.bezeichnung = bezeichnung;
		this.preis = preis;
	}
	
}
